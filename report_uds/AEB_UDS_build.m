function bld_inputs(sys)
%% Blocos From Workspace para todas as entradas do UDS
    FW = {
        'FW_ttc',      'ws_ttc',      0, 600;
        'FW_fsm',      'ws_fsm',      0, 650;
        'FW_brake',    'ws_brake_bar',0, 700;
        'FW_sensf',    'ws_sens_f',   0, 750;
        'FW_crc',      'ws_crc_err',  0, 800;
        'FW_alive',    'ws_alive_err',0, 850;
        'FW_timeout',  'ws_timeout',  0, 900;
        'FW_act',      'ws_act_err',  0, 950;
        'FW_temp',     'ws_ecu_temp', 0, 1000;
        'FW_valid',    'ws_req_valid',0, 1050;
    };
    for r = 1:size(FW,1)
        add_block('simulink/Sources/From Workspace',...
            [sys '/' FW{r,1}],...
            'VariableName', FW{r,3+1},...
            'Position', [FW{r,3}, FW{r,4}, FW{r,3}+120, FW{r,4}+30]);
    end
    for col = 1:9
        nm  = sprintf('FW_req_b%d', col);
        var = sprintf('ws_req_b%d', col);
        add_block('simulink/Sources/From Workspace',...
            [sys '/' nm],'VariableName',var,...
            'Position',[160,(1100+(col-1)*50),280,(1130+(col-1)*50)]);
    end
end

function bld_uds_main(sys)
%% Subsistema principal: AEB_UDS_Handler
    sub = [sys '/AEB_UDS_Handler'];
    add_block('built-in/Subsystem', sub, 'Position',[400,400,700,900]);

    % ── Adicionar MATLAB Function block ──────────────────────────────────
    mf = [sub '/uds_handler'];
    add_block('simulink/User-Defined Functions/MATLAB Function', mf,...
              'Position',[80,80,400,600]);

    % Código injetado via Stateflow API
    sf = sfroot();
    chart = sf.find('-isa','Stateflow.EMChart','Path',[sub '/uds_handler']);
    if ~isempty(chart)
        chart.Script = uds_handler_code();
    end

    % ── Portas do subsistema ──────────────────────────────────────────────
    inputs = {'ttc','fsm_state','brake_bar','sens_fault','crc_err',...
              'alive_err','timeout_err','act_err','ecu_temp',...
              'req_valid','b1','b2','b3','b4','b5','b6','b7','b8','b9'};
    for k = 1:numel(inputs)
        add_block('simulink/Ports & Subsystems/In1',...
            [sub '/In_' inputs{k}],...
            'Position',[20,50+k*40,50,70+k*40]);
    end

    outputs = {'resp_b1','resp_b2','resp_b3','resp_b4','resp_b5',...
               'resp_b6','resp_b7','resp_b8','resp_len',...
               'aeb_enabled','ttc_warn_cal','ttc_l1_cal','kp_cal',...
               'dtc_active','dtc_count','fault_lamp','session_state','sec_unlocked'};
    for k = 1:numel(outputs)
        add_block('simulink/Ports & Subsystems/Out1',...
            [sub '/Out_' outputs{k}],...
            'Position',[500,50+k*35,530,70+k*35]);
    end
end

function bld_outputs(sys)
%% Blocos To Workspace para sinais de saída
    sigs = {'aeb_enabled','ttc_warn_cal','ttc_l1_cal','kp_cal',...
            'dtc_active','dtc_count','fault_lamp','session_state','sec_unlocked',...
            'resp_len'};
    for k = 1:numel(sigs)
        add_block('simulink/Sinks/To Workspace',...
            [sys '/TW_' sigs{k}],...
            'VariableName', sigs{k},...
            'SaveFormat','Array',...
            'Position',[900,300+k*50,1020,320+k*50]);
    end
end

function connect_model(sys)
%% Conexões principais (entradas do workspace → subsistema → saídas)
    inputs_fw = {'FW_ttc','FW_fsm','FW_brake','FW_sensf','FW_crc',...
                 'FW_alive','FW_timeout','FW_act','FW_temp','FW_valid'};
    for k = 1:numel(inputs_fw)
        try
            add_line(sys, [inputs_fw{k} '/1'], ...
                     ['AEB_UDS_Handler/' num2str(k)], 'autorouting','on');
        catch
        end
    end
    for col = 1:9
        try
            add_line(sys, [sprintf('FW_req_b%d',col) '/1'],...
                     ['AEB_UDS_Handler/' num2str(10+col)],'autorouting','on');
        catch
        end
    end
end

%% ═══════════════════════════════════════════════════════════════════════════
%% CÓDIGO DA MATLAB FUNCTION (UDS Handler)
%% ═══════════════════════════════════════════════════════════════════════════

function code = uds_handler_code()
code = [
'function [resp_b1,resp_b2,resp_b3,resp_b4,resp_b5,resp_b6,resp_b7,resp_b8,'...
'resp_len,aeb_enabled,ttc_warn_cal,ttc_l1_cal,kp_cal,'...
'dtc_active,dtc_count,fault_lamp,session_state,sec_unlocked] = ...'...
'uds_handler(ttc,fsm_state,brake_bar,sens_fault,crc_err,'...
'alive_err,timeout_err,act_err,ecu_temp,'...
'req_valid,b1,b2,b3,b4,b5,b6,b7,b8,b9) '...
'%#codegen'...
newline ...
'%% ── Constantes ──────────────────────────────────────────────────────'...
newline ...
'DT_UDS      = 0.1;    % ciclo UDS (s)'...
newline ...
'S3_TIMEOUT  = 5.0;    % timeout sessao (s)'...
newline ...
'DEB_FAULT   = 3;      % debounce falhas de sensor (ciclos)'...
newline ...
'DEB_ALIVE   = 5;      % debounce alive counter (ciclos)'...
newline ...
'DEB_TEMP    = 3;'...
newline ...
'TEMP_MAX    = 85.0;   % temperatura critica (C)'...
newline ...
'SECRET_XOR  = uint32(hex2dec(''A55A3CC3''));'...
newline ...
'SECRET_ADD  = uint32(hex2dec(''12345678''));'...
newline ...
'SEED_INIT   = uint32(hex2dec(''ABCD1234''));'...
newline ...
'%% ── Persistent state ────────────────────────────────────────────────'...
newline ...
'persistent sess s3 seed unlocked dtc dtc_ctr ttc_w ttc_l1 kp_g aeb_en;'...
newline ...
'if isempty(sess)'...
newline ...
'    sess    = uint8(1);       % 1=default 2=extended 3=programming'...
newline ...
'    s3      = 0.0;'...
newline ...
'    seed    = SEED_INIT;'...
newline ...
'    unlocked= uint8(0);'...
newline ...
'    dtc     = zeros(1,7,''uint8'');  % confirmed DTCs (C1001..C1007)'...
newline ...
'    dtc_ctr = zeros(1,7,''uint16''); % debounce counters'...
newline ...
'    ttc_w   = 4.0;   ttc_l1 = 3.0;  kp_g = 10.0;'...
newline ...
'    aeb_en  = uint8(1);'...
newline ...
'end'...
newline ...
'%% ── DTC fault detection ──────────────────────────────────────────────'...
newline ...
'% C1001 radar timeout'...
newline ...
'fault_flags = [timeout_err, 0, 0, crc_err, alive_err, act_err, ...'...
newline ...
'               double(ecu_temp > TEMP_MAX)];'...
newline ...
'deb_lim = [DEB_FAULT,DEB_FAULT,DEB_FAULT,DEB_FAULT,DEB_ALIVE,DEB_FAULT,DEB_TEMP];'...
newline ...
'for i = 1:7'...
newline ...
'    if fault_flags(i) > 0'...
newline ...
'        dtc_ctr(i) = dtc_ctr(i) + uint16(1);'...
newline ...
'        if dtc_ctr(i) >= uint16(deb_lim(i))'...
newline ...
'            dtc(i) = uint8(1);'...
newline ...
'        end'...
newline ...
'    else'...
newline ...
'        dtc_ctr(i) = uint16(0);'...
newline ...
'    end'...
newline ...
'end'...
newline ...
'%% ── Session S3 timer ────────────────────────────────────────────────'...
newline ...
'if sess > 1'...
newline ...
'    s3 = s3 + DT_UDS;'...
newline ...
'    if s3 >= S3_TIMEOUT'...
newline ...
'        sess     = uint8(1);  % retorna a DEFAULT'...
newline ...
'        unlocked = uint8(0);'...
newline ...
'        s3       = 0.0;'...
newline ...
'    end'...
newline ...
'end'...
newline ...
'%% ── Request processing ──────────────────────────────────────────────'...
newline ...
'resp  = zeros(1,8,''uint8'');'...
newline ...
'rlen  = uint8(0);'...
newline ...
'if req_valid > 0'...
newline ...
'    req = uint8([b1,b2,b3,b4,b5,b6,b7,b8,b9]);'...
newline ...
'    SID = req(1);'...
newline ...
'    switch SID'...
newline ...
'      %% 0x10 DiagnosticSessionControl ─────────────────────────────────'...
newline ...
'      case uint8(hex2dec(''10''))'...
newline ...
'        sub = req(2);'...
newline ...
'        if sub==1 || sub==2 || sub==3'...
newline ...
'            sess     = sub;'...
newline ...
'            unlocked = uint8(0);'...
newline ...
'            s3       = 0.0;'...
newline ...
'            resp(1)  = uint8(hex2dec(''50''));  % positive 0x40+SID'...
newline ...
'            resp(2)  = sub;'...
newline ...
'            rlen     = uint8(2);'...
newline ...
'        else'...
newline ...
'            resp     = nrc(SID, uint8(hex2dec(''12'')));  % subFunctionNotSupported'...
newline ...
'            rlen     = uint8(3);'...
newline ...
'        end'...
newline ...
'      %% 0x11 ECUReset ──────────────────────────────────────────────────'...
newline ...
'      case uint8(hex2dec(''11''))'...
newline ...
'        sess     = uint8(1);'...
newline ...
'        unlocked = uint8(0);'...
newline ...
'        s3       = 0.0;'...
newline ...
'        resp(1)  = uint8(hex2dec(''51''));'...
newline ...
'        resp(2)  = req(2);'...
newline ...
'        rlen     = uint8(2);'...
newline ...
'      %% 0x22 ReadDataByIdentifier ─────────────────────────────────────'...
newline ...
'      case uint8(hex2dec(''22''))'...
newline ...
'        did = uint16(req(2))*256 + uint16(req(3));'...
newline ...
'        resp(1) = uint8(hex2dec(''62''));'...
newline ...
'        resp(2) = req(2); resp(3) = req(3);'...
newline ...
'        if did == uint16(hex2dec(''F100''))        % TTC (float32)'...
newline ...
'            fb = float2bytes(single(ttc));'...
newline ...
'            resp(4:7) = fb;  rlen = uint8(7);'...
newline ...
'        elseif did == uint16(hex2dec(''F101''))    % FSM state (uint8)'...
newline ...
'            resp(4) = uint8(fsm_state);  rlen = uint8(4);'...
newline ...
'        elseif did == uint16(hex2dec(''F102''))    % brake_bar (float32)'...
newline ...
'            fb = float2bytes(single(brake_bar));'...
newline ...
'            resp(4:7) = fb;  rlen = uint8(7);'...
newline ...
'        elseif did == uint16(hex2dec(''F103''))    % fault counter (uint8)'...
newline ...
'            resp(4) = uint8(sum(dtc));   rlen = uint8(4);'...
newline ...
'        else'...
newline ...
'            resp = nrc(SID, uint8(hex2dec(''31'')));  % requestOutOfRange'...
newline ...
'            rlen = uint8(3);'...
newline ...
'        end'...
newline ...
'      %% 0x27 SecurityAccess ────────────────────────────────────────────'...
newline ...
'      case uint8(hex2dec(''27''))'...
newline ...
'        sub = req(2);'...
newline ...
'        if sess < 2  % requer sessao Extended ou Programming'...
newline ...
'            resp = nrc(SID, uint8(hex2dec(''22'')));  % conditionsNotCorrect'...
newline ...
'            rlen = uint8(3);'...
newline ...
'        elseif sub == uint8(1)  % RequestSeed'...
newline ...
'            seed = lfsr32(seed);'...
newline ...
'            sb   = typecast(seed, ''uint8'');'...
newline ...
'            resp(1) = uint8(hex2dec(''67''));'...
newline ...
'            resp(2) = uint8(1);'...
newline ...
'            resp(3:6) = sb;'...
newline ...
'            rlen = uint8(6);'...
newline ...
'            unlocked = uint8(0);  % nova semente invalida chave anterior'...
newline ...
'        elseif sub == uint8(2)  % SendKey'...
newline ...
'            recv_key = typecast(req(3:6), ''uint32'');'...
newline ...
'            exp_key  = compute_key(seed, SECRET_XOR, SECRET_ADD);'...
newline ...
'            if recv_key == exp_key'...
newline ...
'                unlocked    = uint8(1);'...
newline ...
'                resp(1)     = uint8(hex2dec(''67''));'...
newline ...
'                resp(2)     = uint8(2);'...
newline ...
'                rlen        = uint8(2);'...
newline ...
'            else'...
newline ...
'                resp = nrc(SID, uint8(hex2dec(''35'')));  % invalidKey'...
newline ...
'                rlen = uint8(3);'...
newline ...
'            end'...
newline ...
'        else'...
newline ...
'            resp = nrc(SID, uint8(hex2dec(''12'')));'...
newline ...
'            rlen = uint8(3);'...
newline ...
'        end'...
newline ...
'      %% 0x2E WriteDataByIdentifier ─────────────────────────────────────'...
newline ...
'      case uint8(hex2dec(''2E''))'...
newline ...
'        if unlocked == 0'...
newline ...
'            resp = nrc(SID, uint8(hex2dec(''33'')));  % securityAccessDenied'...
newline ...
'            rlen = uint8(3);'...
newline ...
'        else'...
newline ...
'            did = uint16(req(2))*256 + uint16(req(3));'...
newline ...
'            val = bytes2float(req(4:7));'...
newline ...
'            if did == uint16(hex2dec(''F200''))'...
newline ...
'                ttc_w = val;'...
newline ...
'            elseif did == uint16(hex2dec(''F201''))'...
newline ...
'                ttc_l1 = val;'...
newline ...
'            elseif did == uint16(hex2dec(''F202''))'...
newline ...
'                kp_g = val;'...
newline ...
'            end'...
newline ...
'            resp(1) = uint8(hex2dec(''6E''));'...
newline ...
'            resp(2) = req(2); resp(3) = req(3);'...
newline ...
'            rlen    = uint8(3);'...
newline ...
'        end'...
newline ...
'      %% 0x14 ClearDiagnosticInformation ──────────────────────────────'...
newline ...
'      case uint8(hex2dec(''14''))'...
newline ...
'        dtc     = zeros(1,7,''uint8'');'...
newline ...
'        dtc_ctr = zeros(1,7,''uint16'');'...
newline ...
'        resp(1) = uint8(hex2dec(''54''));'...
newline ...
'        rlen    = uint8(1);'...
newline ...
'      %% 0x19 ReadDTCInformation ────────────────────────────────────────'...
newline ...
