%% AEB_Controller_build.m
% Gera AEB_Controller.slx: controlador AEB completo (MIL — Model-in-the-Loop)
%
% Implementação fiel ao código C embarcado:
%   aeb_ttc.c    — TTC = d / v_rel, d_brake = v_ego² / (2 × DECEL_L3)
%   aeb_fsm.c    — FSM 7 estados com histerese, warning_min, post-brake
%   aeb_pid.c    — PI Kp=10, Ki=0.05, anti-windup [0,50], jerk limit 100 m/s³
%   aeb_alert.c  — mapeamento estado → alertas visuais/sonoros
%
% Subsistemas:
%   TTC_Calc       — cálculo de TTC e d_brake
%   Driver_Override — detecção de intervenção do motorista
%   AEB_FSM        — FSM completa (7 estados, timers, histerese)
%   PID_Brake      — controlador PI + limitação de jerk
%   Alert_Logic    — geração de alertas (CAN 0x300)
%
% Requisitos: MATLAB R2025b + Simulink

clear; close all; clc;

%% ── 0. Setup ─────────────────────────────────────────────────────────────────
script_dir = fileparts(mfilename('fullpath'));
mdl = 'AEB_Controller';
if bdIsLoaded(mdl), close_system(mdl, 0); end
new_system(mdl); open_system(mdl);
sys = mdl;

set_param(sys, ...
    'Solver','ode4','SolverType','Fixed-step','FixedStep','0.001',...
    'StartTime','0','StopTime','10',...
    'SignalLogging','on','SignalLoggingName','logsout');

%% ── 1. Cenário padrão CCRs 50 km/h ──────────────────────────────────────────
% Gera os sinais de entrada pre-computando a física do evento CCRs.
% O controlador reproduzirá exatamente estas transições de estado.
DT   = 0.001;
TEND = 10.0;
t    = (0:DT:TEND)';
N    = length(t);

% ── Parâmetros (aeb_config.h) ──────────────────────────────────────────────
TTC_W=4.0; TTC_L1=3.0; TTC_L2=2.2; TTC_L3=1.8;
D_L1=20.0; D_L2=10.0; D_L3=5.0;
DECEL_L1=2.0; DECEL_L2=4.0; DECEL_L3=6.0;
V_MIN=1.39;  V_MAX=16.67;
DECEL_MAX=10.0;
WARN_MIN=0.8;  % aviso mínimo antes de frear (s)
HYST=0.2;      % histerese de desescalada (s)
POST=2.0;      % duração POST_BRAKE (s)

% ── Integração física do cenário ──────────────────────────────────────────
v_ego   = zeros(N,1);  v_ego(1)   = 13.89;  % 50 km/h
d_tgt   = zeros(N,1);  d_tgt(1)   = 50.0;   % distância inicial ao alvo
v_tgt   = zeros(N,1);                        % alvo parado
a_ego   = zeros(N,1);
v_ego_ref = zeros(N,1);  % velocidade do alvo para v_rel

% FSM simplificada para pré-computação
st=1; state_t=0; warn_acc=0; deb_t=0; pb_t=0;
decel_map = [0,0,0,DECEL_L1,DECEL_L2,DECEL_L3,0];

for i = 2:N
    vp = v_ego(i-1); dp = d_tgt(i-1); vt = v_tgt(i-1);
    v_rel_p = vp - vt;

    % TTC e d_brake
    if v_rel_p > 0.5
        ttc_p = min(dp / v_rel_p, 10);
    else
        ttc_p = 10;
    end
    d_brake_p = vp^2 / (2*DECEL_L3);
    is_cl = double(v_rel_p > 0);

    % Desired state
    if ~is_cl
        des = 1;
    elseif ttc_p <= TTC_L3 || d_brake_p >= dp
        des = 5;
    elseif ttc_p <= TTC_L2
        des = 4;
    elseif ttc_p <= TTC_L1 || dp <= D_L1
        des = 3;
    elseif ttc_p <= TTC_W
        des = 2;
    else
        des = 1;
    end
    % Distance floor
    if is_cl
        if dp <= D_L3 && des < 5, des = 5; end
        if dp <= D_L2 && des < 4, des = 4; end
        if dp <= D_L1 && des < 3, des = 3; end
    end

    % FSM transitions
    state_t = state_t + DT;
    if st == 6  % POST_BRAKE
        pb_t = pb_t + DT;
        if pb_t >= POST, st=1; pb_t=0; state_t=0; end
    elseif des > st
        if des >= 2 && st == 1
            st = 2; state_t=0; warn_acc=0; % entrar sempre em WARNING primeiro
        elseif st == 2 && warn_acc >= WARN_MIN
            st = des; state_t=0;
        end
    elseif des < st
        deb_t = deb_t + DT;
        if deb_t >= HYST
            if st >= 3
                if des >= 3, st=des;
                else, st=6; pb_t=0; end
            else
                st = des;
            end
            deb_t=0; state_t=0;
        end
    else
        deb_t = 0;
    end
    if st == 2, warn_acc = warn_acc + DT; end

    % Deceleration and kinematics
    decel = decel_map(st+1);
    a_ego(i) = -decel;
    v_ego(i) = max(0, vp - decel*DT);
    if v_ego(i) < 0.01, st=6; pb_t=0; state_t=0; end  % parou: POST_BRAKE
    d_tgt(i) = max(0.5, dp - vp*DT);
    v_tgt(i) = 0;  % alvo parado
end

v_rel    = max(0, v_ego - v_tgt);
ttc_ws   = zeros(N,1);
d_brake  = v_ego.^2 / (2*DECEL_L3);
for i=1:N
    vr = v_rel(i);
    if vr > 0.5, ttc_ws(i) = min(d_tgt(i)/vr, 10);
    else,        ttc_ws(i) = 10; end
end

% Confiança, habilitação, override
confidence   = 15*ones(N,1);
aeb_enabled  = ones(N,1);
steer_angle  = zeros(N,1);
brake_pedal  = zeros(N,1);

% Exportar workspace
ws_vars = {'t','v_ego','d_tgt','v_rel','v_tgt','a_ego','ttc_ws','d_brake',...
           'confidence','aeb_enabled','steer_angle','brake_pedal'};
for k=1:numel(ws_vars)
    vn = ws_vars{k};
    if strcmp(vn,'t'), assignin('base','t',t);
    else, assignin('base',['ws_' vn],[t, eval(vn)]); end
end

%% ── 2. Construir subsistemas ─────────────────────────────────────────────────
bld_ttc_calc(sys);
bld_driver_override(sys);
bld_aeb_fsm(sys);
bld_pid_brake(sys);
bld_alert_logic(sys);

%% ── 3. Blocos From Workspace ────────────────────────────────────────────────
fw = {
    'FW_d_tgt',     'ws_d_tgt',    [50, 80,180, 95];
    'FW_v_ego',     'ws_v_ego',    [50,110,180,125];
    'FW_v_tgt',     'ws_v_tgt',    [50,140,180,155];
    'FW_v_rel',     'ws_v_rel',    [50,170,180,185];
    'FW_a_ego',     'ws_a_ego',    [50,200,180,215];
    'FW_conf',      'ws_confidence',[50,250,180,265];
    'FW_aeb_en',    'ws_aeb_enabled',[50,280,180,295];
    'FW_steer',     'ws_steer_angle',[50,360,180,375];
    'FW_bpedal',    'ws_brake_pedal',[50,390,180,405];
};
for k=1:size(fw,1)
    add_block('simulink/Sources/From Workspace',[sys '/' fw{k,1}],...
        'VariableName',fw{k,2},'Position',fw{k,3},'Interpolate','off');
end

%% ── 4. Posicionar subsistemas ────────────────────────────────────────────────
set_param([sys '/TTC_Calc'],       'Position',[280, 80,430,170]);
set_param([sys '/Driver_Override'],'Position',[280,360,430,420]);
set_param([sys '/AEB_FSM'],        'Position',[530, 80,700,230]);
set_param([sys '/PID_Brake'],      'Position',[780, 80,940,170]);
set_param([sys '/Alert_Logic'],    'Position',[780,200,940,290]);

%% ── 5. Conexões top-level ────────────────────────────────────────────────────
% TTC_Calc ← fontes
add_line(sys,'FW_d_tgt/1',  'TTC_Calc/1','autorouting','on');
add_line(sys,'FW_v_ego/1',  'TTC_Calc/2','autorouting','on');
add_line(sys,'FW_v_tgt/1',  'TTC_Calc/3','autorouting','on');

% Driver_Override ← fontes
add_line(sys,'FW_steer/1',  'Driver_Override/1','autorouting','on');
add_line(sys,'FW_bpedal/1', 'Driver_Override/2','autorouting','on');

% AEB_FSM ← TTC_Calc + fontes + Driver_Override
add_line(sys,'TTC_Calc/1',      'AEB_FSM/1','autorouting','on');  % ttc
add_line(sys,'TTC_Calc/2',      'AEB_FSM/2','autorouting','on');  % d_brake
add_line(sys,'TTC_Calc/3',      'AEB_FSM/3','autorouting','on');  % is_closing
add_line(sys,'FW_d_tgt/1',      'AEB_FSM/4','autorouting','on');  % distance
add_line(sys,'FW_v_ego/1',      'AEB_FSM/5','autorouting','on');  % v_ego
add_line(sys,'FW_v_rel/1',      'AEB_FSM/6','autorouting','on');  % v_rel
add_line(sys,'FW_conf/1',       'AEB_FSM/7','autorouting','on');  % confidence
add_line(sys,'FW_aeb_en/1',     'AEB_FSM/8','autorouting','on');  % aeb_enabled
add_line(sys,'Driver_Override/1','AEB_FSM/9','autorouting','on'); % override

% PID_Brake ← AEB_FSM + a_ego
add_line(sys,'AEB_FSM/2',  'PID_Brake/1','autorouting','on');  % decel_target
add_line(sys,'FW_a_ego/1', 'PID_Brake/2','autorouting','on');  % a_ego (feedback)
add_line(sys,'AEB_FSM/1',  'PID_Brake/3','autorouting','on');  % fsm_state

% Alert_Logic ← AEB_FSM
add_line(sys,'AEB_FSM/1',  'Alert_Logic/1','autorouting','on'); % fsm_state

%% ── 6. To Workspace e Scope ──────────────────────────────────────────────────
tw = {
    'TW_fsm_state',    'fsm_state',    [1100, 70,1200, 85],  'AEB_FSM/1';
    'TW_decel_tgt',    'decel_target', [1100,100,1200,115],  'AEB_FSM/2';
    'TW_brake_active', 'brake_active', [1100,130,1200,145],  'AEB_FSM/3';
    'TW_alert_level',  'alert_level',  [1100,160,1200,175],  'AEB_FSM/4';
    'TW_warn_timer',   'warn_timer',   [1100,190,1200,205],  'AEB_FSM/5';
    'TW_state_timer',  'state_timer',  [1100,220,1200,235],  'AEB_FSM/6';
    'TW_brake_pct',    'brake_pct',    [1100,280,1200,295],  'PID_Brake/1';
    'TW_brake_bar',    'brake_bar',    [1100,310,1200,325],  'PID_Brake/2';
    'TW_ttc_out',      'ttc_out',      [1100,370,1200,385],  'TTC_Calc/1';
    'TW_d_brake_out',  'd_brake_out',  [1100,400,1200,415],  'TTC_Calc/2';
    'TW_is_closing',   'is_closing',   [1100,430,1200,445],  'TTC_Calc/3';
    'TW_alert_type',   'alert_type',   [1100,490,1200,505],  'Alert_Logic/1';
    'TW_alert_active', 'alert_active', [1100,520,1200,535],  'Alert_Logic/2';
    'TW_buzzer',       'buzzer_cmd',   [1100,550,1200,565],  'Alert_Logic/3';
    'TW_override',     'override_out', [1100,610,1200,625],  'Driver_Override/1';
};
for k=1:size(tw,1)
    blk = [sys '/' tw{k,1}];
    add_block('simulink/Sinks/To Workspace',blk,...
        'VariableName',tw{k,2},'SaveFormat','Array','Position',tw{k,3});
    add_line(sys,tw{k,4},[tw{k,1} '/1'],'autorouting','on');
end

% Scope (FSM state + brake pressure + TTC)
add_block('simulink/Sinks/Scope',[sys '/Scope_AEB'],...
    'NumInputPorts','4','Position',[1100,650,1180,730]);
add_line(sys,'AEB_FSM/1',  'Scope_AEB/1','autorouting','on');
add_line(sys,'PID_Brake/2','Scope_AEB/2','autorouting','on');
add_line(sys,'TTC_Calc/1', 'Scope_AEB/3','autorouting','on');
add_line(sys,'AEB_FSM/5',  'Scope_AEB/4','autorouting','on');

%% ── 7. Auto-layout e salvamento ──────────────────────────────────────────────
Simulink.BlockDiagram.arrangeSystem(sys,'FullLayout','on');
lns = find_system(sys,'FindAll','on','type','line');
if ~isempty(lns), Simulink.BlockDiagram.routeLine(lns); end

save_system(mdl, fullfile(script_dir,[mdl '.slx']));
fprintf('\n=== AEB_Controller.slx salvo em: %s ===\n\n', script_dir);
fprintf('Uso:\n');
fprintf('  sim(''AEB_Controller'')                    — CCRs 50 km/h\n');
fprintf('  AEB_Controller_scenarios(''ccrs_30'')      — 30 km/h\n');
fprintf('  AEB_Controller_scenarios(''ccrm'')         — alvo em movimento\n');
fprintf('  AEB_Controller_scenarios(''override'')     — override do motorista\n');
fprintf('  AEB_Controller_scenarios(''fault_sensor'') — falha de sensor\n');
fprintf('  AEB_Controller_plot                       — plotar resultados\n\n');

