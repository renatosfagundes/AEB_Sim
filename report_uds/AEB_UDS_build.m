%% AEB_UDS_build.m
% Gera AEB_UDS.slx: camada de diagnóstico UDS (ISO 14229) do sistema AEB
%
% Serviços UDS implementados:
%   0x10 — DiagnosticSessionControl (Default / Extended / Programming)
%   0x11 — ECUReset
%   0x14 — ClearDiagnosticInformation
%   0x19 — ReadDTCInformation (subfunção 0x02 — reportDTCByStatusMask)
%   0x22 — ReadDataByIdentifier  (DIDs F100-F103)
%   0x27 — SecurityAccess — LFSR-32 seed + algoritmo seed-key
%   0x2E — WriteDataByIdentifier (DIDs F200-F202) — requer desbloqueio
%   0x31 — RoutineControl (0x0301 enable/disable AEB, 0x0302 self-test)
%   0x3E — TesterPresent (suprime timeout S3)
%
% DTCs modelados (7):
%   C1001 Radar timeout    C1002 Lidar timeout     C1003 Plausibilidade
%   C1004 CRC error        C1005 Alive stuck        C1006 Falha atuador
%   C1007 Sobre-temperatura
%
% Criptografia (SID 0x27):
%   Semente: LFSR Galois 32 bits (polinômio 0x80200003)
%   Chave: key = ROTL32(seed ^ 0xA55A3CC3, 7) + 0x12345678  (mod 2^32)
%
% Requisitos: MATLAB R2025b + Simulink

clear; close all; clc;

%% ── 0. Setup ─────────────────────────────────────────────────────────────────
script_dir = fileparts(mfilename('fullpath'));
mdl = 'AEB_UDS';
if bdIsLoaded(mdl), close_system(mdl, 0); end
new_system(mdl); open_system(mdl);
sys = mdl;

set_param(sys, ...
    'Solver','ode4','SolverType','Fixed-step','FixedStep','0.1',...
    'StartTime','0','StopTime','30',...
    'SignalLogging','on','SignalLoggingName','logsout');

%% ── 1. Cenário nominal: pré-computa entradas para 30 s @ 100 ms ──────────────
DT   = 0.1;
TEND = 30.0;
t    = (0:DT:TEND)';
N    = length(t);

% ── Sinais de sistema (saídas do controlador / percepção) ──────────────────
ttc_in      = 10 * ones(N,1);           % TTC (s)
fsm_in      = ones(N,1);                % FSM state (1=STANDBY)
brake_bar   = zeros(N,1);               % Pressão de freio (bar)
sens_fault  = zeros(N,1);               % Falha sensor percepção
crc_err     = zeros(N,1);               % Erro CRC CAN
alive_err   = zeros(N,1);               % Alive counter travado
timeout_err = zeros(N,1);               % Timeout CAN
act_err     = zeros(N,1);               % Falha atuador (pressão ≠ comandada)
ecu_temp    = 25 * ones(N,1);           % Temperatura ECU (°C)

% ── Sequência de requisições UDS (simulação do tester externo) ────────────
% Formato: cada linha = [t_inicio, SID, B2, B3, B4, B5, B6, B7, B8]
% Padding com zeros; req_valid=1 apenas no ciclo da requisição
req_frames = [
    %  t     SID    B2    B3    B4    B5    B6    B7    B8
     1.0, 0x10, 0x03,    0,    0,    0,    0,    0,    0;  % Extended session
     2.0, 0x22, 0xF1, 0x00,    0,    0,    0,    0,    0;  % ReadDID TTC
     3.0, 0x22, 0xF1, 0x01,    0,    0,    0,    0,    0;  % ReadDID FSM state
     4.0, 0x3E, 0x00,    0,    0,    0,    0,    0,    0;  % TesterPresent
     % Falha injetada em t=5s (sensor fault → DTC C1001 confirmado em t=8s)
     8.0, 0x19, 0x02, 0xFF,    0,    0,    0,    0,    0;  % ReadDTC (all)
     % Security Access: RequestSeed
    10.0, 0x27, 0x01,    0,    0,    0,    0,    0,    0;
     % Security Access: SendKey (pré-calculado para seed=0xABCD1234)
     % key = ROTL32(0xABCD1234 ^ 0xA55A3CC3, 7) + 0x12345678
     % seed ^ mask = 0xABCD1234 ^ 0xA55A3CC3 = 0x0E972EF7
     % ROTL32(0x0E972EF7, 7) = 0x74B97F87 (aprox — calculado abaixo)
     % valor correto preenchido pelo cenário
    11.0, 0x27, 0x02, 0x74, 0xB9, 0x7F, 0x87,    0,    0;  % placeholder
     % Write TTC Warning = 3.5 s (DID F200)
    12.0, 0x2E, 0xF2, 0x00, 0x40, 0x60, 0x00, 0x00,    0;  % 3.5f em IEEE-754
     % Routine: Disable AEB
    15.0, 0x31, 0x01, 0x03, 0x01,    0,    0,    0,    0;
     % Routine: Enable AEB
    20.0, 0x31, 0x01, 0x03, 0x01,    1,    0,    0,    0;  % sub=enable
     % Clear all DTCs
    25.0, 0x14, 0xFF, 0xFF, 0xFF,    0,    0,    0,    0;
     % Confirm DTCs cleared
    27.0, 0x19, 0x02, 0xFF,    0,    0,    0,    0,    0;
     % TesterPresent (keep session)
    28.0, 0x3E, 0x00,    0,    0,    0,    0,    0,    0;
];

% Calcular chave correta para seed 0xABCD1234
seed0 = uint32(0xABCD1234);
key0  = uds_compute_key(seed0);
kb = typecast(key0,'uint8');
% Atualiza linha do SendKey
idx = find(req_frames(:,1) == 11.0);
req_frames(idx, 4:7) = double(kb);

% Montar vetores de sinal
req_bytes = zeros(N, 9);   % [SID, B2..B8, valid]
req_valid = zeros(N, 1);
for r = 1:size(req_frames,1)
    k = round(req_frames(r,1) / DT) + 1;
    if k >= 1 && k <= N
        req_bytes(k,:) = req_frames(r,2:end);
        req_valid(k)   = 1;
    end
end

% ── Injeção de falhas ──────────────────────────────────────────────────────
% C1001: radar timeout t=5..25s  (dura até ClearDTC em t=25s)
timeout_err(round(5/DT)+1 : round(25/DT)) = 1;
% C1004: CRC error t=13..14s
crc_err(round(13/DT)+1 : round(14/DT)) = 1;
% C1007: over-temp t=16..19s
ecu_temp(round(16/DT)+1 : round(19/DT)) = 90;

% ── Exportar workspace ────────────────────────────────────────────────────
ws_ttc       = [t, ttc_in];
ws_fsm       = [t, fsm_in];
ws_brake_bar = [t, brake_bar];
ws_sens_f    = [t, sens_fault];
ws_crc_err   = [t, crc_err];
ws_alive_err = [t, alive_err];
ws_timeout   = [t, timeout_err];
ws_act_err   = [t, act_err];
ws_ecu_temp  = [t, ecu_temp];
ws_req_valid = [t, req_valid];

% req_bytes: 9 colunas → exporta como sinal escalar, mux de 9 canais
for col = 1:9
    assignin('base', sprintf('ws_req_b%d', col), [t, req_bytes(:,col)]);
end

vars = {'ws_ttc','ws_fsm','ws_brake_bar','ws_sens_f','ws_crc_err',...
        'ws_alive_err','ws_timeout','ws_act_err','ws_ecu_temp','ws_req_valid'};
for k = 1:numel(vars)
    assignin('base', vars{k}, eval(vars{k}));
end

fprintf('Workspace UDS configurado (%d amostras, DT=%.1f s).\n', N, DT);

%% ── 2. Criar subsistema AEB_UDS ──────────────────────────────────────────────
bld_uds_main(sys);
bld_inputs(sys);
bld_outputs(sys);
connect_model(sys);

%% ── 3. Salvar ────────────────────────────────────────────────────────────────
save_path = fullfile(script_dir, [mdl '.slx']);
save_system(mdl, save_path);
fprintf('Modelo salvo: %s\n', save_path);
fprintf('\nPara simular:\n');
fprintf('  AEB_UDS_scenarios(''dtc_lifecycle'');\n');
fprintf('  out = sim(''AEB_UDS'');\n');
fprintf('  AEB_UDS_plot(out);\n');

